    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int i= tabel.getSelectedRow();
        if(i>-1){
            kode_item.setText(model.getValueAt(i, 0).toString());
            nama_item.setText(model.getValueAt(i, 1).toString());
            kategori_item.setText(model.getValueAt(i, 2).toString());
            favorite_skin.setText(model.getValueAt(i, 3).toString());
        }
    }//GEN-LAST:event_tabelMouseClicked

